from app.services.llm import ask_llm


def generate_feedback(user_answer: str, reference: str, keywords: list[str]) -> str:
    message = f"""
    Ты строгий технический интервьюер.
    Оцениваешь только содержание ответа.
    Не хвалишь без причины.
    Не фантазируешь.
    Отвечаешь структурировано.

    Вот эталонный ответ:
    {reference}

    Вот ответ кандидата:
    {user_answer}

    Вот ключевые пункты, которые должны быть упомянуты:
    {", ".join(keywords)}

    Проанализируй ответ кандидата и:

    1. Укажи, какие ключевые моменты отсутствуют.
    2. Объясни, что раскрыто недостаточно глубоко.
    3. Укажи неточности (если есть).
    4. Дай рекомендации по улучшению содержания.

    В ответе укажи только пункт 4.

    Не переписывай полностью ответ.
    Дай структурированный краткий фидбек.
    """
    return ask_llm(message)

